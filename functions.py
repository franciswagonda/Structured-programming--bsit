def square_of_number(x):
    return x * x    
print(square_of_number(9))

def multiply_numbers(a, b):
    return a * b
print(multiply_numbers(6, 7))

def divide_numbers(a, b):
    if b == 0:
        return "Error: Division by zero is not allowed."
    return a / b    
print(divide_numbers(700,9))



